/* Using Conditional Statement */

const age = 60;

if (age >= 60){
    console.log("Elderly");
}else if (age <= 17){
    console.log("Too young");
}else if (age >= 18){
    console.log("Middle")
}

/* Using Conditional Statement */

const grade = 60;

if (grade > 100){
    console.log("Invalid");
}else if (grade >= 98 ){
    console.log("With highest honor");
}else if (grade >= 95){
    console.log("With high honor");
}else if (grade >= 90){
    console.log("With honor");
}else if (grade >= 75){
    console.log("Passed");
}else if (grade <= 74){
    console.log("Failed")
}